begin
	# Declare our Turing model
	@model function jmodel(x,ey,y)
	    # Our prior belief
	    theta ~ Uniform(-0.5 * π, 0.5 * π)
	    q_perp ~ Normal(0.,5.)
	    #
	    m = tan.(theta)
	    q = q_perp./cos.(theta)
	    #
	    #for i in 1:length(y)
	    #    y[i] ~ Normal.(m*x .+ q,ey)
	    #end
	    return y ~ MvNormal(m*x .+ q,ey)
	end
	
