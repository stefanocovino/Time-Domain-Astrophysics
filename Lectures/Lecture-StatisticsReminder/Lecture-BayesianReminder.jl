md"""
## Information criteria and hypothesis testing
***

- In a frequentist scenario hypothesis testing is often carried out comparing the [likelihood ratio](https://en.wikipedia.org/wiki/Likelihood-ratio_test) of different models or the residuals by, e.g. a [F-test](https://en.wikipedia.org/wiki/F-test).

    - These tests are either related to the assumptions that data are drawn from a Normal parental distributions or do not take into account the number of parameters of the models. There are more limitations we don't discuss here (see, e.g., [Protassov et al. 2002](https://ui.adsabs.harvard.edu/abs/2002ApJ...571..545P/abstract)).
 

- In a Bayesian scenario everything we can know given the data and the prior information is stated by the Bayes’ Theorem:

```math
p(M,\theta|D,I) = \frac{p(D|M,\theta,I)p(M,\theta|I)}{p(D|I)}
```

- The posterior probability for model $M$ can be computed by the posterior distribution of the parameters after having marginalized (i.e. integrated) over the parameter space.

- Assuming to have two (or more) competing models, computing the *evidence*, the denominator of the previous expression, we can evaluate the **odds ratio**:

```math
O_{21} \equiv \frac{p(M_2|D,I)}{p(M_1|D,I} = \frac{p(M_2|I)}{p(M_1|I)} \frac{p(D|M_2)}{p(D|M_1)} = \frac{p(M_2|I)}{p(M_1|I)} B_{ij}
```

- The first factor of the odds ratio is the prior odd ratio, and the second factor is called the **Bayes factor**.

- This is a very sophisticated approach but, often, computationally hard to apply. There are simpler tools, although with limitations and assumptions.



- Let's follow a simple example, just to understand how a Bayes factor works: we are comparing two models: $M_1$, with a single parameter. $\theta$, and $M_0$ with $\theta$ fixed at a given value $\theta_0$. So, $M_0$ has no free parameters.

- In most cases the likelikood function, $\mathcal{L}(\theta) = p(D|\theta,M_1)$, is much more peaked than the prior, so that the prior is essentially constant over the range of significant likelyhood. Therefore, if $\hat{\theta}$ is the maximum of likelyhood, we can write:

```math
p(D|M_1) \sim p(\hat{\theta}|M_1) \int \mathcal{L}(\theta) d\theta
```

- Then, we write the integral of the likelihood as the product of its maximum times a characteristi width (e.g., a full width at haf maximum, etc.):

```math
p(D|M_1) \sim p(\hat{\theta}|M_1) \mathcal{L}(\hat{\theta}) \delta\theta
```

- And finally, for clarity, we write $p(\hat{\theta}|M_1)$ as the reciprocal of a characteristic width of the prior, $\Delta \theta$. Thus:

```math
p(D|M_1) \sim \mathcal{L}(\hat{\theta}) \frac{\delta\theta}{\Delta \theta}
```

- Since $M_0$ has no free parameters $p(D|M_0) = \mathcal{L}(\theta_0)$, and the Bayes factor turns out to be:

```math
B_{10} \sim \frac{\mathcal{L}(\hat{\theta})}{\mathcal{L}(\theta_0)} \frac{\delta\theta}{\Delta \theta}
```

- The likelihood ratio can never be less than unity, since $\theta_0$ is within the possible range of $\theta$, and the second factor penalizes the likelyhood ratio for the "wasted" parameter space that gets ruled out by the data ($\delta\theta$ is narrower than $\Delta\theta$).

> In any case, a full discussion of hypothesis testing in a Bayesian scenario is a very interesting topic, but definitely beyond the purpose of the corse. Nevertheless, during the course, we will often mention this matter. 


- There are simpler alternatives, although with further assumptions and limitations. 
    - However, they require to compute only the maximum likelihood, and not the whole posterior distribution of paramaters.

- A well known criterion is the [Bayesian (or Schwarz) Information Criterior](https://en.wikipedia.org/wiki/Bayesian_information_criterion) (BIC):

```math
BIC \equiv -2 \log [L^0(M)] + k \log N
```

- where $L^0[M]$ is the maximum likelihood for model $M$, $k$ the number of model parameters and $N$ is the number of data points.

- Another frequently applied criterion is the [Akaike Information Criterion](https://en.wikipedia.org/wiki/Akaike_information_criterion) (AIC):

```math
AIC \equiv -2 \log [L^0(M)] + 2k + \frac{2k(k+1)}{N-k-1}
```

- When multiple models are compared, the one with the smallest AIC or BIC is the best model to select. If the models are equally successful in describing the data (they have the same value of $L^0[M]$, then the model with fewer free parameters "wins".

"""

# ╔═╡ 59006f2e-eb6b-45c8-9b2a-d5eeefa05978
