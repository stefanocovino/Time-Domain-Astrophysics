md"""
# The [Gregory-Loredo (1992)](https://ui.adsabs.harvard.edu/abs/1992ApJ...398..146G/abstract) algorithm

***

- We assume to have a set of $N$ arrival times, $D = {t_j}$, with $j = 1$ to $N$, over a some observing interval of duration $T$.

- We do not put any constrain on the arrival rate, $r(t)$.


"""

# ╔═╡ 0e4c318a-511c-4646-9b33-634e5cb87c6d
cm"""
## The Likelihood function
***

- The probability of ``D`` given some rate function, ``r(t)``, the likelihoof function is computed as follows:
     - The observing interval is divided in small time intervals ``\Delta t_i``, each containing either 1 or zero evewnts.
     - Adopting the Poissonian distribution, the probability of seeing ``n`` events in an interval ``\Delta t`` about time ``t`` is:

```math
p_n(t) = \frac{[r(t) \Delta t]^n e^{-r(t) \Delta t}}{n!}
```

- We also assume the rate can be considered constant within ``\Delta t``

- If ``N`` and ``Q`` are the number of time intervals with 1 or no events we can write:

```math
p(D|r,I) = \prod_{i=1}^N p_1(t_i)  \prod_{k=1}^Q p_0(t_k)
```

- For no event we have: ``p_0(t) = e^{-r(t) \Delta t}`` and for one event we have: ``p_1(t) = r(t) \Delta t e^{-r(t) \Delta t}``, so that:

```math
p(D|r,I) = \Delta t^N \prod_{i=1}^N r(t_i) e^{-\sum_{k=1}^{N+Q} r(t_k) \Delta t} = \Delta t^N \prod_{i=1}^N r(t_i) e^{\int_T r(t) dt}
```

- where the sum of ``r(t)\Delta t`` over all the observed intervals is expressed by the integral of the rate over the intervals with a range of integration ``T=(N+Q)\Delta t``.

- ``T`` is the total duration of the observtions and includes all the intervals. These intervals do not need to be contiguous, therefore allowing to remove intervals when, e.g., the detectors were not operational.

"""

# ╔═╡ 78e65245-8981-4bdf-863a-e8497bb8e2e6
cm"""

## Constant model
***

- The simplest model for the data has a constant event rate, ``A``. We denote this one-parameter model as ``M_1``.

- Setting ``r(t) = A`` we have:

```math 
p(D | A, M_1) = \Delta t^N A^N e^{-AT}
```

- We also assume a uniform prior for ``A`` from ``0`` to an upper limit ``A_{\rm max}``: ``~ p(A | M_1) = \frac{1}{A_{\rm max}}``. The dependence on ``A_{\rm max}`` turns out to be very weak. 

"""

# ╔═╡ c448e9d9-d6b3-4bd7-a797-c072cfd1ca20
cm"""

## Periodic stepwise model
***

- Our model for a periodic signal is a stepwise function with a constant rate ion each of the m bins per period ``(m \ge 2)``.
- This is indeed a class of models, one for each choice of ``m``, denoted as ``m_m``.
- Each model has ``m+2`` parameters: the angular frequency ``\omega (\omega = 2 \pi / P``), a phase ``\phi`` and ``m`` values ``r_j``, specifying the rate in each phase bin.

- It is possible to compute the phase bin ``j``, from ``1`` to ``m``, at any arrival time ``t`` with:

```math
j(t) = {\rm int} \{ 1 + m [( \omega t + \phi) ~ mod ~ 2 \pi]/2\pi \}
```

- For a stepwise model the average rate ``A`` is: ``A = \frac{1}{m} \sum_{j=1}^m r_j`` and the light-curve shape is totally described by the fraction of the total rate per period that is in each phase bin. These fractions are:

```math
f_j = \frac{r_j}{\sum_{k=1}^m r_k} =  \frac{r_j}{mA}
```

- so that ``r_j = mAf_j$ and $\sum_{j=1}^m f_j = 1``.

- Therefore, the likelihood function becomes:

```math
p(D | \omega, \phi, A, f, M_m) = \Delta t^N \left( \prod_{j=1}^N mA f_{j(t_i)} \right) e^{-\sum_{k=1}^{N+Q} mAf_{j(t_k)} \Delta t} = 
```

```math
= \Delta t^N (mA)^N \left( \prod _{j=1}^N f_j^{n_j} \right) e^{-mA \sum_{j=1}^m f_j \tau_j(\omega,\phi)} 
```

- where ``n_j = n_j(\omega,\phi)`` is the number of events that fall into phase bin ``j``, and ``\tau_j(\omega,\phi)`` is the total integration time  for bin ``j``.

- We expect that the integration time per bin will ``\approx T/m``, so that: ``mA\sum_{j=1}^m f_j \tau_j = AT \sum_{j=1}^m f_j \frac{\tau_j}{T/m} = AT \sum_{j=1}^m f_j s_j``, where `` s_j (\omega, \phi) = \frac{\tau_j (\omega, \phi)}{T/m}  `` is known as bin time factor.

- If the observing interval, ``T``, is a contiguous interval containing  an integral number of periods, then  ``\tau_j = T/m`` so ``s_j = 1`` for all ``j``.
    - More generally, the observing interval will not be an integral number for periods and may have gaps, so that ``s_j`` will differ from unity.
    - As long as the number of periods in the observing interval is large, and gaps are not concentrated in certain bins, ``s_j \approx 1`` and ``AT \sum_{j=1}^m f_j \approx AT``.

- Then the likelihood is:

```math
p(D | \omega, \phi, A, f, M_m) = \Delta t^N (mA)^N e^{-AT} \left( \prod_{j=1}^m f_j^{n_j} \right) 
```

"""

# ╔═╡ aee3ea98-92a3-42a7-b990-801059e964d3
cm"""

### Priors for periodic model parameters
***

- We assume we have no prior information linking frequency, phase and light-curve shape so that priors for these quantities are independent of one another.

- The prior density for ``\phi`` we take to be uniform over the interval ``[0,2\pi)``: ``p(\phi | M_m) = 1/2\pi``.

- An invariance argument requiring indepence of the prior for the frequency from the time units leads to the prior density for ``\omega`` of the form: ``p(\omega | M_m) = 1/\omega \ln (\omega_{\rm hi} / \omega_{\rm lo})``, where ``[\omega_{\rm lo}, \omega_{\rm hi}]`` is the prior range for ``\omega``.

- It might happen we know the frequency and the phase a priori, in which case the priors for frequency and phase are just ``\delta``-functions.

- For similar arguments, the prior for the average rate is: ``p(A | M_m) = 1/A_{\rm max}``.

- Finally, we assume that in each bin ``j`` all possible values between 0 and 1 are equiprobable for ``f_j`` subject to the constraint that ``\sum f_j = 1``. We then have: ``p(f | M_m) = K_m \delta (1-\sum_{j=1}^m f_j)``. The normalization constant ``K_m`` can be evaluated by the requirement that ``\int p(f|M_m) df = 1``.

    - The evaluation of the integral is shown in Gregory & Loredo (1992), [Sect. 4.3](https://ui.adsabs.harvard.edu/abs/1992ApJ...398..146G/abstract), and turns out to be: ``K_m = (m-1)!``.

"""

# ╔═╡ f53afb0d-3415-49d0-ad08-2de1c6bf165d
cm"""

### Priors for model comparison
***

- We consider the hypotheses of the presence and absence of a periodic signal equally probable a priori.
    - Thus we assign equal probabolity ``1/2`` to the claass of nonperiodic and periodic models.
 
- The class of nonperiodic model includes only the case with ``m=1``, therefore ``p(M_1|I) = 1/2``.

- The periodic class includes ``\nu = m_{\rm max} - 1`` models, i.e., with ``m`` from ``2`` to ``m_{\rm max}``. Since we consider each member of this class equally probable a priori, the ``1/2`` class probability is equally spread amonmg the ``\nu`` class component. Therefore ``p(M_m|I) = 1/2\nu``.

"""

# ╔═╡ 20709427-62ce-43aa-b217-6f76b28c530d
cm"""

## Odds ratio for signal detection
***

- We test the hypopthesis that there is a periodic signal comparing members of the class of stepwise models to the constant model.

- The probability for the nonperiodic model is:

```math
p(M_1 | D,I) = \frac{1}{1+\sum_{m=2}^{m_{\rm max}} O_{m1}} 
``` 

- where ``O_{m1}`` is the odds ratio for each periodic model over the constant model.

- The probability that the signal is periodic is:

```math
p(m > 1 | D,I) = \frac{\sum_{m=2}^{m_{\rm max}} O_{m1}}{1+\sum_{m=2}^{m_{\rm max}} O_{m1}} 
```

- or, simply, ``1-p(M_1 | D,I)``.

- Therefore, the odds ratio in favor of the hypothesis that the signal is periodic is the ratio of the probabilities above:

```math
O_{\rm per} = \sum_{m=2}^{m_{\rm max}} O_{m1} 
```

"""

# ╔═╡ 630467e3-a671-40c0-9817-635aba687919
cm"- There are several possible cases to consider: period and phase known, period known but phase unknown and period and phase both unknown."

# ╔═╡ a208a380-4dc2-4e1c-8965-cfda2c6ae730
cm"""

### Period and phase known
***

- This is little more than a didactic case, yet the gloabal likelihood (aka evidence) for the constant model is:

```math
p(D | M_1) = \int_0^{A{_{\rm max}}} p(A|M_1) p(D|M_1,A,I) dA = 
```

```math
= \frac{\Delta t^N}{A{_{\rm max}}} \int _0^{A{_{\rm max}}} A^N e^{-AT} = \frac{\Delta t^N \gamma(N+1,A{_{\rm max}}T)}{A{_{\rm max}}T^{N+1}}
```

- where ``\gamma(n,x)`` denotes the [incomplete gamma function](https://en.wikipedia.org/wiki/Incomplete_gamma_function).





- The evidence for the periodic models is (details of the derivation in Gregory & Loredo (1992), [Sect. 5.](https://ui.adsabs.harvard.edu/abs/1992ApJ...398..146G/abstract):

```math
p(D|\omega,\phi,M_m) = \int df \int_0^{A{_{\rm max}}} p(A|M_m)~p(f|M_m)~p(D|\omega,\phi,A,f,M_m) dA =
```

```math
= \frac{\Delta t^N ~(m-1)! ~N! ~\gamma(N+1,A{_{\rm max}} T)}{A{_{\rm max}} ~T^{N+1} ~(N+m-1)!} \frac{m^N}{W_m(\omega,\phi)}
```

- where ``W_m(\omega,\phi) = \frac{N!}{n_1! n_2! \dots n_m!}``, is the multiplicity of the binned distribution of events, i.e., the number of ways the particular set of ``n_j`` values can be obtained by distributing ``N`` events in ``m`` bins.

- Calculating the odds ratio several common factors cancel, and we get:

```math
O_{m1} (\omega,\phi) = \frac{1}{\nu} \binom{N+m-1}{N}^{-1} \frac{m^N}{W_m(\omega,\phi)} 
```

- Then, the odds ratio in favor of the periodic class of models over the constant model is:

```math
O_{\rm per} (\omega,\phi) = \sum_{m=2}^{m_{m_{\rm max}}} O_{m1} (\omega,\phi) 
```

"""

# ╔═╡ 6c02d53e-4cd1-410a-987f-f4ab6c69c1b2
cm"""

### Period known, phase unknown
***

- This is a case of possible practicle interest, e.g., if the period is known from previous observations.

- The evidence is now:

```math
p(D|\omega,M_m) = \int_0^{2\pi} \int df \int_0^{A{_{\rm max}}} p(\phi|M_m)~p(A|M_m)~p(f|M_m)~p(D|\omega,\phi,A,f,M_m) dA =
```

```math
= \frac{\Delta t^N ~(m-1)! ~N! ~\gamma(N+1,A{_{\rm max}} T)}{2\pi~A{_{\rm max}} ~T^{N+1} ~(N+m-1)!} \times \int_0^{2\pi} \frac{m^N}{W_m(\omega,\phi)} d\phi
```

- The odds ratio in favor of an ``m``-bin model with known frequency becomes:

```math
O_{m1} (\omega) = \frac{1}{2 \pi \nu} \binom{N+m-1}{N}^{-1} \int_0^{2\pi} \frac{m^N}{W_m(\omega,\phi)} d\phi
```

- The odds ratio in favor of a periodic model is computed as above.

"""

# ╔═╡ 89a5e0f4-289c-4777-8c62-92a1a448b920
cm"""

### Period and phase unknown
***

- This is the more general case. The evidence becomes:

```math
p(D|M_m) = \frac{\Delta t^N ~(m-1)! ~N! ~\gamma(N+1,A{_{\rm max}} T)}{2\pi~A{_{\rm max}} ~T^{N+1} ~(N+m-1)! \ln (\omega_{\rm hi} / \omega_{lo})} \times \int_{\omega_{\rm lo}}^{\omega_{\rm hi}} \frac{d\omega}{\omega}  \int_0^{2\pi} \frac{m^N}{W_m(\omega,\phi)} d\phi 
```

- where again ``\omega_{\rm lo}`` and ``\omega_{\rm hi}`` are the lowest and highest frequency considered.

- And the odds ratio in favor of ``M_m`` with unknown period and phase is:

```math
O_{m1} = \frac{1}{2 \pi \nu \ln (\omega_{\rm hi} / \omega_{lo})} \binom{N+m-1}{N}^{-1} \int_{\omega_{\rm lo}}^{\omega_{\rm hi}} \frac{d\omega}{\omega}  \int_0^{2\pi} \frac{m^N}{W_m(\omega,\phi)} d\phi
```

"""

# ╔═╡ c6b0678f-588c-4303-9912-c98ba415fd6d
cm"""

## Estimation of the frequency
***

- Assuming we can choose a given model, in our case a given number of bins ``m``, the posterior for the fequency is ``p(\omega|D,M_m) = p(\omega|M_m)~p(D|\omega,M_m) / p(D | M_m)``.

- Substituting our expressions:

```math
p(\omega|D,M_m) = \frac{C}{\omega} \int \frac{1}{W_m(\omega,\phi)} d\phi 
```

- where ``C = \left[ \int_{\omega_{\rm lo}}^{\omega_{\rm hi}} \frac{d\omega}{\omega} \int_0^{2\pi} \frac{1}{W_m(\omega,\phi)} d\phi \right]^{-1}``






- Rather than choosing the "best" bin number, within the class of periodic model, we can compute the posterior for the frequency considering the whole set of periodic model weighted for the evidence:

```math
p(\omega | m>1, D, I) = \sum_{m=2}^{m_{\rm max}} p(M_m, \omega | m > 1, D, I) = \sum_{m=2}^{m_{\rm max}} p(M_m | D,I)~p(\omega | D, M_m)
```

- where ``p(M_m | D,I) = \frac{O_{i1}}{\sum_{j=1}^{N_{\rm mod}} O_{j1}}`` and ``N_{\rm mod}`` is the total number of models considered. Of course, ``O_{11} = 1``.

"""

# ╔═╡ 6a2ae578-9ccd-43d7-ba89-e8f0e5d2a060
cm"""

## Estimation of the light-curve shape
***

- Knowing the light-curve shape can often be of interest.

- Solving the problem in the more general way is considerably complex, and probably not really useful (see Gregory & Loredo 1992; [Sect. 7](https://ui.adsabs.harvard.edu/abs/1992ApJ...398..146G/abstract)).

- Else, one can simplify the approacdh factorizing the shape distribution as follows:

```math
p(M_m,\omega,\phi,f)|D,I) = p(M_m|D,I)~p(\omega|D,M_m)~p(\phi|\omega,D,M_m)~p(f|\omega,\phi,D,M_m)
```

- and, essentially, one first identifies the most probable number of bins, then the most probable frequency and then the most probable phase given the previous choices.  After this choices one can estimates the most probable light-curve shape, and it turns out be that the solution is essentially what one could get just computing:

```math
f_j = n_j(\omega,\phi)/N
```

- where of course ``n_j`` is the number of events falling in bin number ``j``.

- This might be a reasomnable solution if the odds ratios for the number of bins shows a prominent single peak, but this is not true when there is not a clear prefered bin number and in any case this approach cannot offer any information about the shape uncertainty.

- The joint distribution for ``f`` turns out to be (see Gregory & Loredo 1992; [Sect. 7.1](https://ui.adsabs.harvard.edu/abs/1992ApJ...398..146G/abstract)):

```math
p(f|\omega,\phi,D,M_m) = \frac{(N+M-1)!}{N!} W_m(\omega,\phi) \prod_{j=1}^m f_j^{n_j}
```

- It can be shown that the previous relation is maximized at ``\hat{f}_j = \frac{n_j}{N}``, the mode of the distrinution of the ``f_j``.

"""

# ╔═╡ 8ba338e8-f334-416f-9a4d-664c35083c67
md"""

- The conditional mean of the ``f_j`` distribution could instead be coomputed with (see Gregory & Loredo 1992; [Sect. 7.2](https://ui.adsabs.harvard.edu/abs/1992ApJ...398..146G/abstract)):

```math
<f_j|\omega,\phi,m> = \int f_j~p(f|\omega,\phi,D,M_m) df 
```

- giving: ``<f_j|\omega,\phi,m> = \frac{n_j+1}{N+m}``, and the mean and the mode, as expected, converge to the same value if ``n_j >> 1`` and ``N >> m``.

- Computing the second moment of ``f_j`` one can derive:

```math
\sigma_j = \sqrt{\frac{<f_j>(1-<f_j>)}{N+m+1}} 
```


"""

# ╔═╡ 57ed9979-fdd8-4cf3-9d4d-0ffc098c7849
md"""

### Light-curve shape averaged over frequency and phase
***

- It is possible to obtain a result marginalizing the frequency and the phase:

```math
<f(t) | m > = \int  \int \int f_{j(t)} p(\omega,\phi,f|D,M_m) df d\phi d\omega =
```

```math
= \int \int \left[ \int f_{j(t)}~p(f|\omega,\phi,D,M_m) df \right] p(\omega,\phi|D,M_m) d\phi  d\omega = 
```

```math
= \int \int <f_{j(t)}|\omega,\phi,D,M_m>~p(\omega,\phi|D,M_m) d\phi d\omega 
``` 

- i.e, integrals based on quantitieds already evaluated. The same could be carried out for the second moment of ``f_{j(t)}`` to obtain the standard deviation.

"""

# ╔═╡ e2a0f3f8-a3a1-4bb4-871d-5f5b1595a794
md"""

### Superposing stepwise models
***

- As already mentioned above, often the most probable bin number is highly favoured, yet there are cases when several possible bin numbers are almost equiprobable.

- Thus a better estimate of the light-curve shape comes from marginalizing over ``m`` as well as the various model parameters.

- We then compute the posterior mean for ``r(t)/A = mf(t)`` as follows:

```math
<r(t)/A | m>1> = \sum_{m=2}^{m_{\rm max}} m \int \int \int f_{j(t)} p(M_m,\omega,\phi,f|D,I) df d\phi d\omega = 
```

```math
= \sum_{m=2}^{m_{\rm max}} m p(M_m|D,I) \int \int \left[ \int f_{j(t)}~p(f|\omega,\phi,D,M_m) df \right] p(\omega,\phi|D,M_m) d\phi d\omega = 
```

```math
= \sum_{m=2}^{m_{\rm max}} m p(M_m|D,I)<f(t)|m>
```

- where the last average was computed above. And again an analogues equation holds for the second moment.


"""

# ╔═╡ 364403ee-ff1f-45e8-82ac-b17f8834c0ce
md"""

## A modified solution for significant data gaps
***

- One assumptiopn of this algorithm is that the "detector" is working properly for the whole duration of the observations.
    - Phenomena as "dead time", etc. can of course invalidate this assumption, and techniques to deal with these problems have been developed although at the price of an increased complexity.
      
- A different case can happen if we can just assume to have a data-taking of a given length but with observation gaps, i.e. the detection efficiency can just be 1 or 0.

    - A satellite in low orbit monitoring some source in the sky could observe the target for a given amount of time but the observations are interruoted by the possible obscuration of the field by the Earth.
 
- This is a case that can be embedded in this algorithm rather easily.

- First, we write the rate, ``r_j``, as `` r_j = mAf_j / s_j ``, where, as already defined, the bin time factor, ``s_j``, is ``s_j (\omega, \phi) = \frac{\tau_j (\omega, \phi)}{T/m}  ``.

- Introducing the bin time factors the light-curve is now described by ``h_j = f_j/s_j``, and ``f_ = \frac{r_js_j}{mA} = \frac{r_j\tau_j}{AT} ``.

- Since ``\sum f_j \tau_j = AT`` we have:

```math
p(D|\omega.\phi,A,f,M_m) = \Delta t^N (mA)^N e^{-AT} S(\omega,\phi) \left( \prod_{j=1}^m f_j^{n_j} \right) 
```

- where ``S(\omega,\phi)`` is the binning factor:

```math
S(\omega,\phi) = \prod_{j=1}^m s_j^{-n_j} 
```

- The ``s_j`` are completely determined by the data and the model parameters ``\omega, \phi, `` and ``m``.






- After this reparametrization we have, e.g., that the odds ratio in favor of an ``m``-bin model with known frequency becomes:

```math
O_{m1} (\omega) = \frac{1}{2 \pi \nu} \binom{N+m-1}{N}^{-1} \int_0^{2\pi} \frac{S(\omega,\phi)~m^N}{W_m(\omega,\phi)} d\phi
```

- Fibally, we consider the effect of the binning factor on the estimate of the light-curve shape. Since the shape is now described by ``h_j = f_j/s_j`` we now have:

```math
\hat{h}_j = \frac{n_j}{s_jN} 
```

```math
<h_j|\omega,\phi,m> = \frac{n_j+1}{s_j(N+m)} 
```

```math
\sigma_j = \frac{1}{s_j} \sqrt{\frac{s_j<h_j>(1-s_j<h_j>)}{N+m+1}} 
```

"""

